package model;

/**
 *
 * @author HoaiNam
 */
public class Buong {
    private int id;
    private String name;
    private String local;
    private String type;

    public Buong() {
    }

    public Buong(int id, String name, String local, String type) {
        this.id = id;
        this.name = name;
        this.local = local;
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLocal() {
        return local;
    }

    public void setLocal(String local) {
        this.local = local;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

}
